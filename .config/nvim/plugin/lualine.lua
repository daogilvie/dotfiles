local lsp_stat = require('lsp-status')
local function lsp_progress_msg()
  return vim.trim(lsp_stat.status_progress())
end

require('lualine').setup {
  options = { theme = 'nord' },
  sections = {
    lualine_a = { 'mode' },
    lualine_b = { 'branch', 'diff', 'diagnostics' },
    lualine_c = { 'filename' },
    lualine_x = { 'encoding', 'filetype', lsp_progress_msg },
    lualine_y = { 'progress' },
    lualine_z = { 'location' }
  },
  inactive_sections = {
    lualine_a = {},
    lualine_b = {},
    lualine_c = { 'filename' },
    lualine_x = { 'location' },
    lualine_y = {},
    lualine_z = {}
  }
}
